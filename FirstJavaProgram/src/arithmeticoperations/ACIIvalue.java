package arithmeticoperations;

public class ACIIvalue {
	public static void main(String[] args)   
	{  
	char c1 = 'a';  
	char c2 = 'b';  
	int a1 = (int) c1;  
	int a2 = (int) c2;  
	System.out.println("The ASCII value of " + c1 + " is: " + a1);  
	System.out.println("The ASCII value of " + c2 + " is: " + a2);  
	}  
}
